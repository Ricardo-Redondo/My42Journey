int	ft_iterative_power(int nb, int power)
{
	int	i;
	int v;

	i = 1;
	v = nb;
	if (power < 0)
		return (0);
	else if (power == 0)
		return (1);
	else if (power == 1)
		return (nb);
	else
	{
		while (i < power)
		{
			nb *= v;
			i++;
		}
		return (nb);
	}
}
