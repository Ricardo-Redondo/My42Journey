#include <unistd.h>

void	ft_print_comb(void)
{
	int	a;
	int	b;
	int	c;

	a = '0';
	b = a + 1;
	c = b + 1;
	while (a <= '7')
	{
		write (1, &a, 1);
		write (1, &b, 1);
		write (1, &c, 1);
		if (a != '7')
		{
			write (1, ",", 1);
		}
		a++;
		b = a + 1;
		c = b + 1;
	}
}

